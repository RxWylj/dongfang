function getImageWidth(url,callback){
    var img = new Image();
    img.src = url;
    
    // 如果图片被缓存，则直接返回缓存数据
    if(img.complete){
        callback(img.width, img.height);
    }else{
            // 完全加载完毕的事件
        img.onload = function(){
        callback(img.width, img.height);
        }
    }
    
}
